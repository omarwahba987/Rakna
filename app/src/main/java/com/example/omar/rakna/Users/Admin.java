package com.example.omar.rakna.Users;

public class Admin {
    String id , email ,type ;

    public Admin() {
    }

    public Admin(String id, String email, String type) {
        this.id = id;
        this.email = email;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
